def save_rolling_backtest(
    sport: str,
    *,
    end_date: str,
    window_days: int,
    start_date: str,
    model: str,
    total_games: int,
    games_picked: int,
    correct_picks: int,
    win_rate: Optional[float],
    flat_units: float,
    kelly_units: float,
    max_drawdown: float,
    daily_units,
    game_log,
) -> None:
    """
    Upsert one row of the rolling cache. `daily_units` is a list of
    {day, units, kelly_units, cum_units, cum_kelly_units} dicts.
    """
    now = datetime.datetime.utcnow().isoformat(timespec='seconds')
    payload = (
        sport, end_date, int(window_days), start_date, model,
        int(total_games), int(games_picked), int(correct_picks),
        win_rate, float(flat_units), float(kelly_units), float(max_drawdown),
        json.dumps(list(daily_units)),
        _game_log_to_json(game_log),
        now,
    )
    conn = _connect()
    try:
        with conn:
            conn.execute(
                'INSERT INTO rolling_backtest_cache '
                '(sport, end_date, window_days, start_date, model, '
                ' total_games, games_picked, correct_picks, win_rate, '
                ' flat_units, kelly_units, max_drawdown, '
                ' daily_units_json, game_log_json, computed_at) '
                'VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?) '
                'ON CONFLICT(sport, end_date, window_days, model) DO UPDATE SET '
                ' start_date       = excluded.start_date, '
                ' total_games      = excluded.total_games, '
                ' games_picked     = excluded.games_picked, '
                ' correct_picks    = excluded.correct_picks, '
                ' win_rate         = excluded.win_rate, '
                ' flat_units       = excluded.flat_units, '
                ' kelly_units      = excluded.kelly_units, '
                ' max_drawdown     = excluded.max_drawdown, '
                ' daily_units_json = excluded.daily_units_json, '
                ' game_log_json    = excluded.game_log_json, '
                ' computed_at      = excluded.computed_at',
                payload,
            )
    finally:
        conn.close()


def load_rolling_backtest(
    window_days: int,
    end_date: Optional[str] = None,
    model: str = 'logreg',
    *,
    include_game_log: bool = False,
) -> list[dict]:
    """
    Return rolling-cache rows for a given window. If `end_date` is None,
    returns rows whose end_date is the latest cached value per sport.
    """
    cols = (
        'sport, end_date, window_days, start_date, model, '
        'total_games, games_picked, correct_picks, win_rate, '
        'flat_units, kelly_units, max_drawdown, '
        'daily_units_json, computed_at'
    )
    if include_game_log:
        cols = cols + ', game_log_json'

    conn = _connect()
    try:
        if end_date is not None:
            rows = conn.execute(
                f'SELECT {cols} FROM rolling_backtest_cache '
                'WHERE window_days = ? AND model = ? AND end_date = ? '
                'ORDER BY sport',
                (int(window_days), model, end_date),
            ).fetchall()
        else:
            # Latest row per sport for this window
            rows = conn.execute(
                f'SELECT {cols} FROM rolling_backtest_cache AS r '
                'WHERE window_days = ? AND model = ? '
                '  AND end_date = (SELECT MAX(end_date) FROM rolling_backtest_cache '
                '                  WHERE sport = r.sport AND window_days = r.window_days '
                '                    AND model = r.model) '
                'ORDER BY sport',
                (int(window_days), model),
            ).fetchall()
    finally:
        conn.close()

    keys = [
        'sport', 'end_date', 'window_days', 'start_date', 'model',
        'total_games', 'games_picked', 'correct_picks', 'win_rate',
        'flat_units', 'kelly_units', 'max_drawdown',
        'daily_units_json', 'computed_at',
    ]
    if include_game_log:
        keys = keys + ['game_log_json']

    out = []
    for r in rows:
        row = dict(zip(keys, r))
        try:
            row['daily_units'] = json.loads(row.pop('daily_units_json') or '[]')
        except (TypeError, ValueError):
            row['daily_units'] = []
        if include_game_log and row.get('game_log_json'):
            try:
                row['game_log'] = json.loads(row.pop('game_log_json'))
            except (TypeError, ValueError):
                row['game_log'] = []
        out.append(row)
    return out


def rolling_computed_today(
    sport: str, window_days: int, model: str = 'logreg',
) -> bool:
    """
    True if the rolling row for (sport, window_days, model) was computed today.
    Uses UTC date to match the `computed_at` timestamp stored on save, so the
    check is consistent regardless of the host's local timezone.
    """
    today = datetime.datetime.utcnow().date().isoformat()
    conn = _connect()
    try:
        row = conn.execute(
            'SELECT computed_at FROM rolling_backtest_cache '
            'WHERE sport = ? AND window_days = ? AND model = ? '
            'ORDER BY end_date DESC LIMIT 1',
            (sport, int(window_days), model),
        ).fetchone()
    finally:
        conn.close()
    if row is None or not row[0]:
        return False
    return str(row[0]).startswith(today)


def daily_performance(days: int = 30) -> list[dict]:
    """
    Return one entry per day for the last `days` days with daily and cumulative
    flat + kelly units across all settled picks.

    Days with no settled picks still appear (units = 0) so a chart consumer can
    plot a continuous series without re-bucketing.
    """
    today = datetime.date.today()
    start = today - datetime.timedelta(days=max(int(days) - 1, 0))
    conn = _connect()
    try:
        rows = conn.execute(
            'SELECT game_date, '
            '       COALESCE(SUM(flat_units), 0) AS flat, '
            '       COALESCE(SUM(kelly_units), 0) AS kelly '
            'FROM picks_log '
            'WHERE actual IS NOT NULL AND game_date >= ? '
            'GROUP BY game_date',
            (start.isoformat(),),
        ).fetchall()
    finally:
        conn.close()
    by_day = {r[0]: (float(r[1]), float(r[2])) for r in rows}

    series = []
    cum_flat = 0.0
    cum_kelly = 0.0
    day = start
    while day <= today:
        flat, kelly = by_day.get(day.isoformat(), (0.0, 0.0))
        cum_flat += flat
        cum_kelly += kelly
        series.append({
            'day': day.isoformat(),
            'units': flat,
            'kelly_units': kelly,
            'cum_units': round(cum_flat, 4),
            'cum_kelly_units': round(cum_kelly, 6),
        })
        day += datetime.timedelta(days=1)
    return series
