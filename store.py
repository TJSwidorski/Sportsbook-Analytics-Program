def _game_log_to_json(game_log) -> str:
    """Serialize a backtest game_log (list of GameResult dataclasses or dicts)."""
    out = []
    for g in game_log:
        if isinstance(g, dict):
            out.append(g)
            continue
        out.append({
            'game_index': g.game_index,
            'date_or_week': g.date_or_week,
            'pick': g.pick,
            'actual': g.actual,
            'correct': g.correct,
            'units': g.units,
            'kelly_units': g.kelly_units,
            'bet_line': g.bet_line,
            'unit_size': g.unit_size,
            'ev': g.ev,
            'away_lines': g.away_lines,
            'home_lines': g.home_lines,
        })
    return json.dumps(out)


def save_backtest_history(
    sport: str,
    season_year: int,
    model: str,
    *,
    start_date: str,
    end_date: str,
    total_games: int,
    games_picked: int,
    correct_picks: int,
    win_rate: Optional[float],
    flat_units: float,
    kelly_units: float,
    roi_flat: Optional[float],
    roi_kelly: Optional[float],
    max_drawdown: float = 0.0,
    game_log,
) -> None:
    """Upsert one aggregate row. `game_log` is a list of GameResult or dicts."""
    now = datetime.datetime.utcnow().isoformat(timespec='seconds')
    payload = (
        sport, season_year, model,
        start_date, end_date,
        int(total_games), int(games_picked), int(correct_picks),
        win_rate, float(flat_units), float(kelly_units),
        roi_flat, roi_kelly, float(max_drawdown),
        _game_log_to_json(game_log),
        now,
    )
    conn = _connect()
    try:
        with conn:
            conn.execute(
                'INSERT INTO backtest_history '
                '(sport, season_year, model, start_date, end_date, '
                ' total_games, games_picked, correct_picks, win_rate, '
                ' flat_units, kelly_units, roi_flat, roi_kelly, max_drawdown, '
                ' game_log_json, computed_at) '
                'VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?) '
                'ON CONFLICT(sport, season_year, model) DO UPDATE SET '
                ' start_date    = excluded.start_date, '
                ' end_date      = excluded.end_date, '
                ' total_games   = excluded.total_games, '
                ' games_picked  = excluded.games_picked, '
                ' correct_picks = excluded.correct_picks, '
                ' win_rate      = excluded.win_rate, '
                ' flat_units    = excluded.flat_units, '
                ' kelly_units   = excluded.kelly_units, '
                ' roi_flat      = excluded.roi_flat, '
                ' roi_kelly     = excluded.roi_kelly, '
                ' max_drawdown  = excluded.max_drawdown, '
                ' game_log_json = excluded.game_log_json, '
                ' computed_at   = excluded.computed_at',
                payload,
            )
    finally:
        conn.close()


def load_backtest_history(
    sport: Optional[str] = None,
    model: str = 'logreg',
    *,
    include_game_log: bool = False,
) -> list[dict]:
    """
    Return aggregate rows ordered by (sport, season_year DESC).

    `include_game_log=False` (the default) skips the JSON blob to keep
    /api/history responses small. Pass True when consumers (e.g. recent_picks
    rebuilds, audits) actually need the per-game records.
    """
    cols = (
        'sport, season_year, model, start_date, end_date, '
        'total_games, games_picked, correct_picks, win_rate, '
        'flat_units, kelly_units, roi_flat, roi_kelly, max_drawdown, computed_at'
    )
    if include_game_log:
        cols = cols + ', game_log_json'

    conn = _connect()
    try:
        if sport is None:
            rows = conn.execute(
                f'SELECT {cols} FROM backtest_history '
                'WHERE model = ? ORDER BY sport, season_year DESC',
                (model,),
            ).fetchall()
        else:
            rows = conn.execute(
                f'SELECT {cols} FROM backtest_history '
                'WHERE sport = ? AND model = ? ORDER BY season_year DESC',
                (sport, model),
            ).fetchall()
    finally:
        conn.close()

    keys = [
        'sport', 'season_year', 'model', 'start_date', 'end_date',
        'total_games', 'games_picked', 'correct_picks', 'win_rate',
        'flat_units', 'kelly_units', 'roi_flat', 'roi_kelly',
        'max_drawdown', 'computed_at',
    ]
    if include_game_log:
        keys = keys + ['game_log_json']

    out = []
    for r in rows:
        row = dict(zip(keys, r))
        if include_game_log and row.get('game_log_json'):
            try:
                row['game_log'] = json.loads(row.pop('game_log_json'))
            except (TypeError, ValueError):
                row['game_log'] = []
        out.append(row)
    return out


