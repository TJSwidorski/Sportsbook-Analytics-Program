"""
SQLite-backed cache for scraped game data.

Single database file at `data/cache.db` with four tables:

    games               one row per game — odds lists are JSON-encoded
    cached_keys         one row per (sport, cache_key) that has been fetched,
                        including empty days, so we don't re-fetch them forever
    backtest_history    one row per (sport, season_year, model) — aggregate
                        backtest stats persisted by `backtest_history.py`
                        for fast retrieval by /api/history
    picks_log           one row per pick generated by the live prefetch flow.
                        Settled (W/L, units) once the game's scores arrive.
                        Powers the History tab's recent-picks ledger and the
                        rolling cumulative-units chart.

Public API preserves the shape the old parquet store exposed, so all callers
in runner.py, backtest.py, and prefetch.py continue to work:

    save(sport, key, df)
    load(sport, key) -> DataFrame | None
    exists(sport, key) -> bool
    list_available(sport) -> list[str]

`max_cached_date(sport)` is used by the prefetch for incremental gap-fill.

History/log API:

    save_backtest_history(sport, season_year, model, ...)
    load_backtest_history(sport=None, model='logreg') -> list[dict]
    log_picks(sport, cache_key, game_date, picks, model='logreg') -> int
    unsettled_picks(sport=None, max_age_days=30) -> list[dict]
    settle_pick_record(pick_id, away_score, home_score) -> bool
    recent_settled_picks(limit=50) -> list[dict]
    daily_performance(days=30) -> list[dict]
"""

from __future__ import annotations

